    private void init() {
        selectedFactura = FacturasDAO.getFactura(selectedFactura.getNumfac());
        lineasFacturasList = new ArrayList<>((selectedFactura.getLineasFacturases()));
        DefaultTableModel model = (DefaultTableModel) jLineasFacturasTable.getModel();
        model.setRowCount(0);
        lineasFacturasList.forEach(linea -> model.addRow(new Object[]{
                linea.getArticulos().getReferart(),
                linea.getFacturas().getNumfac(),
                linea.getCantidad(),
                linea
        }));

    }
